        valorDolar= 273.03;
        valorEuro = 299.81;
        valorReal = 57.55;
        
        numeroingresado = caja1.getText();//tomo el valor ingresado
        ingresoPars = Double.parseDouble(numeroingresado);//paso de String a Double
        
        seleccion = jComboBox1.getSelectedItem().toString();//tomo el valor elegido en el Combobox
        //
        switch (seleccion){
            case "Dolar": 
              cotizacion = ingresoPars / valorDolar;
                      moneda = "d√≥lares";break; 
            case "Real":
              cotizacion = ingresoPars / valorReal;
                      moneda = "reales";break;
            case "Euro":
              cotizacion = ingresoPars / valorEuro;
                      moneda = "euros";break;
        }    
         Resultado.setText(numeroingresado +"$ son "+ cotizacion+" "+moneda);//seteo el label 
        
        
